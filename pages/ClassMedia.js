// Contructeurs des media 
class Media {
  constructor(arg) {
    this.id = arg.id;
    this.photographerId = arg.photographerId;
    this.title = arg.title;
    this.image = arg.image;
    this.tags = arg.tags;
    this.likes = arg.likes;
    this.date = arg.date;
    this.price = arg.price;
  }
}
